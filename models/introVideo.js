const mongoose = require('mongoose');

const introVideoSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true,
    },
    description: {
        type: String,
        required: false,
        trim: true,
    },
    videoUrl: {
        type: String,
        required: true,
        trim: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

const IntroVideo = mongoose.model('IntroVideo', introVideoSchema);

module.exports = IntroVideo;
